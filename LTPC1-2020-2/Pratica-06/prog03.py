# Operações matemáticas
numero_1  =  float ( input ( "Numero 1:" ))
numero_2  =  float ( input ( "Numero 2:" ))

imprimir ( "Soma:" , numero_1 + numero_2 )
imprimir ( "Subtração:" , numero_1 - numero_2 )
print ( "Multiplicação:" , numero_1 * numero_2 )
print ( "Divisão:" , numero_1 / numero_2 )
print ( 'Divisão Inteira:' , numero_1 // numero_2 )
print ( "Resto da Divisão:" , numero_1 % numero_2 )
print ( 'Potência:' , numero_1 ** numero_2 )
imprimir ( "Raiz:" , numero_1 ** ( 1. / numero_2 ))
